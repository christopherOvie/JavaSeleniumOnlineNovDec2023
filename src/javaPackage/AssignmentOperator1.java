package javaPackage;

public class AssignmentOperator1 {

	public static void main(String[] args) {
		int a =10;
		a +=3;  //13 do not pass space between  +  and =
		System.out.println(a);  //13
		
		a-=4;
		System.out.println(a);  //9
		
		a*=2;
		System.out.println(a);  //18
		
		a/=2;
		
		System.out.println(a); //9
	}

}
