package javaPackage;

public class IfElse {

	public static void main(String[] args) {
		int q=400;  
		int w=400;
		//loca variable
		if(q<w) {
			System.out.println("q is lesser");
		}
		else if(q==w){
			System.out.println("q is equal to w");
		}
		else {
			System.out.println("q is greater than w");
		}
	}

}
